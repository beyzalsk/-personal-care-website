<p id="yazı" style="margin-left:20px; ">
	<strong>Cilt bakımı</strong>, cildin ihtiyaçlarına uygun ürünleri düzenli olarak kullanarak elde edilen sonuçlar bütünüdür diyebiliriz. Herkes farklı cilt yapılarına sahiptir. Cilt yapısını tanıyıp eksikleri gidermek adına bakım yapmak oldukça önemlidir. Böylece daha sağlıklı görünen, aydınlık ciltlere sahip olunabilir. Cildin eksiklerini görmemek, cilt tipine uygun olmayan ürünler kullanmak akne, yara vb. farklı cilt sorunlarının oluşmasına yol açabilmektedir. Bu nedenle doğru cilt bakımı yapmak son derece önemlidir.Sağlıklı ve ışıltılı bir cilde sahip olmak hem güzel bir görüntü için hem de ruh-beden sağlığı için önemli bir unsurdur. Kişinin cilt tipine uygun düzenli olarak bakımların yapılması zamanla ve yaşam koşullarıyla birlikte meydana gelebilecek kırışıklık, sarkma, leke, akne gibi sorunların oluşmasını engellemek açısından da oldukça önemlidir. Cilt bakımında amaç güzellik gibi görünse de öncelik cilt sağlığının korunması olmalıdır.<strong>Cilt bakımının yararları </strong> Kendi cildinize özel ürünler ile cilt bakımı rutinleri oluşturmak sağlığınız açısından oldukça yararlıdır. Örneğin kuru cildinize nem sağlayacak ürünler kullanmak yüzünüzdeki pul pul dökülen deriyi yok eder ve cildinizi daha canlı bir hale dönüştürür. Aynı şekilde aktif sivilceler ile baş etmeye çalışıyorsanız akne önleyici temizleyicileri cilt bakım rutininize ekleyebilir ve ileride oluşacak aknelere önceden çözüm bulabilirsiniz. Ciltteki fazla kuruluk, yağlılık hissi, sivilceler, yaralar vb. tüm sorunlar aslında sizleri psikolojik olarak da etkiler. Hepimiz pürüzsüz, canlı, parlak ve aknesiz ciltlere sahip olmak isteriz. Bu nedenle cilt bakımını ihmal etmemek ve cildin eksiklerini giderecek ürünler kullanmamız gereklidir. Bu yararlanırın olması için <a href="#b1">günlük</a> ve <a href="#c2">günlük bakım dışında</a> da yapılması gereken şeyler var. 
</p> 



	
