<tr> <h3> 3. ADIM NEMLENDİRME İŞLEMİ</h3>
<p id="yazı">
	İster yağlı istersen hassas bir cilde sahip ol, cildi nemlendirmek en önemlisi. Uzun adımlar içeren bir cilt bakım rutini uygulamasan bile cildini düzenli olarak temizlemek ve nemlendirmek senin için vazgeçilmez olmalı. Özellikle kuru bir cilde sahipsen, nemlendirme adımı cildinin kuru görünümünü giderecek, pul pul dökülmesine engel olacak. Yağlı bir cilde sahipsen, nemlendirici kullandığında cildinin daha fazla yağlanacağı önyargısını ise bir kenara bırakmalısın. Jel formüldeki yağsız nemlendiricileri tercih ederek cildinin nem dengesini sağlayabilir, böylece cildinin daha fazla sebum üretmesine engel olabilirsin. Ciltte ağırlık yaratmayacak, içerik bakımından zengin bir nemlendiriciyi cilt bakım rutinine dahil ettiğinde, cildinin çok daha taze, canlı, nemli ve ışıl ışıl sağlıklı göründüğünü fark edeceksin.Göz çevresinin, cildin geri kalanına oranla çok daha hassas olduğunu düşünürsek, nemsiz kalması anında ince çizgilere davetiye çıkarabilir. Bu yüzden göz çevrene de nemlendirici sürmeyi atlamamalısın. 
</p>
